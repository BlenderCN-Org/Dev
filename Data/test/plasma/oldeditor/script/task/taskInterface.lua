--==============================================================================
-- TaskInterface
--==============================================================================

local count = 0

function TaskInterface( params )
   local this = params or {}
   
   -----------------------------------------------------------------------------
   -- Methods
   -----------------------------------------------------------------------------
   
   -----------------------------------------------------------------------------
   function this.close()
   end
   -----------------------------------------------------------------------------
   function this.save()
   end
   -----------------------------------------------------------------------------
   function this.open()
   end
   -----------------------------------------------------------------------------
   function this.hotkey( ev )
   end
   -----------------------------------------------------------------------------
   function this.keyPress( ev )
   end
   -----------------------------------------------------------------------------
   function this.keyRelease( ev )
   end
   -----------------------------------------------------------------------------
   function this.hid( ev )
   end
   
   -----------------------------------------------------------------------------
   --  Data members
   -----------------------------------------------------------------------------
   this.name  = this.name or "default"
   this.id    = count
   
   count      = count+1
   
   -----------------------------------------------------------------------------
   return this
end